function moltiplicazione() {
    numero1 = document.getElementById("numero1").value;
    numero2 = document.getElementById("numero2").value;
    document.getElementById("result").innerHTML = (numero1*numero2);
}

function divisione() {
    numero1 = document.getElementById("numero1").value;
    numero2 = document.getElementById("numero2").value;
    document.getElementById("result").innerHTML = (numero1/numero2);
}